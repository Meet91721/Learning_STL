#include <bits/stdc++.h>

using namespace std;
int main()
{
    // LEFT SHIFT
    int i = 201;
    int n = 2;
    cout << "This is left shift: " << n << " << " << i << " = " << (i << n) << endl;

    // RIGHT SHIFT
    i = 201;
    n = 2;
    cout << "This is right shift: " << n << " >> " << i << " = " << (i >> n);
}